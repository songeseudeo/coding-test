import java.util.Scanner;

public class Solution {
    public static void main(String[] args) {
        //Scanner 객체를 생성해 입력 받을 준비를 함.
        Scanner sc = new Scanner(System.in);
        // 입력된 문자열 한 줄을 읽어 a 변수에 담는다.
        String a = sc.next();
        //a에 담긴 내용을 출력
        System.out.println(a);
    }
}